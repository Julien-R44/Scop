#include "libmath.h"

t_vec3		ft_vec3cross(t_vec3 a, t_vec3 b)
{
	return (VEC3(
		(a.y * b.z) - (a.z * b.y),
		(a.z * b.x) - (a.x * b.z),
		(a.x * b.y) - (a.y * b.x)
	));
}
