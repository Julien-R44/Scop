#include "libmath.h"

t_mat4		ft_mat4identity(double value)
{
	return ((t_mat4) {
		{value, 0.f, 0.f, 0.f},
		{0.f, value, 0.f, 0.f},
		{0.f, 0.f, value, 0.f},
		{0.f, 0.f, 0.f, value}
	});
}
